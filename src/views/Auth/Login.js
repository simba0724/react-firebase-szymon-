const INITIAL_STATE = {
  email: '',
  password: '',
  error: null,
};

class LogInFormBase extends React.Component {
  constructor(props) {
    super(props);

    this.state = { ...INITIAL_STATE };
  }

  onSubmit = event => {
    const { email, password } = this.state;

    this.props.firebase
      .doSignInWithEmailAndPassword(email, password)
      .then((user) => {
        this.setState({ ...INITIAL_STATE });
        this.props.history.push("/");
        window.toast("Logged in Successfully!");
        // window.location.href = "/dashboard";
      })
      .catch(error => {
        console.log(error)
        window.alert(error.code);
        this.setState({ error });
      });

    event.preventDefault();
  };

  onChange = event => {
    this.setState({ [event.target.name]: event.target.value });
  };

  render() {
    const { email, password, error } = this.state;

    const isInvalid = password === '' || email === '';

    return(
      <form className={this.props.classes.form} onSubmit={this.onSubmit} noValidate>
        <TextField
          variant="outlined"
          margin="normal"
          required
          fullWidth
          id="email"
          label="Email Address"
          name="email"
          autoComplete="email"
          value={email}
          onChange={this.onChange}
          autoFocus
        />
        <TextField
          variant="outlined"
          margin="normal"
          required
          fullWidth
          name="password"
          label="Password"
          type="password"
          id="password"
          value={password}
          onChange={this.onChange}
          autoComplete="current-password"
        />
        {/*<FormControlLabel
          control={<Checkbox value="remember" color="primary" />}
          label="Remember me"
        />*/}
        <Button
          type="submit"
          fullWidth
          variant="contained"
          color="primary"
          disabled={isInvalid}
          className={this.props.classes.submit}
        >
          Sign In
        </Button>
      </form>
    )
  }
}

const LoginForm = compose(
  withFirebase,
)(LogInFormBase);

